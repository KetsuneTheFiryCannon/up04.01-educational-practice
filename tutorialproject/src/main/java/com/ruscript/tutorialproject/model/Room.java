package com.ruscript.tutorialproject.model;

public class Room {
    public int RoomId;

    public String RoomName;

    public Room() {
    }

    public int getRoomId() {
        return RoomId;
    }

    public void setRoomId(int roomId) {
        RoomId = roomId;
    }

    public String getRoomName() {
        return RoomName;
    }

    public void setRoomName(String roomName) {
        RoomName = roomName;
    }
}
